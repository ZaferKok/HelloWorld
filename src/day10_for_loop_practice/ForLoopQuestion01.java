package day10_for_loop_practice;

public class ForLoopQuestion01 {

	public static void main(String[] args) {
		/*Type a program to find the sum of first 
		 * 10 natural numbers and print it on the console*/

		
		int sum = 0;
		for (int i=0; i<10; i++) {
			
		sum= sum+i;	//Create a variable assign 0 for addition
		}			// Type sum = sum + i or sum+=i
		System.out.println(sum); // Put out of the body
	
	}

}
