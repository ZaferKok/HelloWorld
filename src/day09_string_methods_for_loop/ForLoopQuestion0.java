package day09_string_methods_for_loop;

public class ForLoopQuestion0 {

	public static void main(String[] args) {
	
		// type a program the numbers from 3 to 50 on the console
		
		for (int i = 3; i <= 50; i++) {
			System.out.print(i+", ");
			
		}
		System.out.println();
		for (int i = 50; i >= 3; i--) {
		System.out.print(i+", ");
		}
	}
}
